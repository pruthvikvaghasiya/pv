package double_class;

public class dclass {
    static int i;
    static String s;
    static float f;
    static double d;
    static boolean b;

    public static void main(String[] args) {

        i=10;
        s="pruthvik";
        f=10.02F;
        d=20.43;
        b=true;

        System.out.println("integer =" + i);
        System.out.println("string =" + s);
        System.out.println("float =" + f);
        System.out.println("double =" + d);
        System.out.println("boolean =" + b);
    }
}
