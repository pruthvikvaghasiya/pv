package Operator;

public class comparison {
    public static void com(){      // Comparison Operator
        int a=10,b=20;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }

    public static void main(String[] args) {
        com();
    }
}
